#Uniary operator(only one variable with single operator)
print("Uniary operator")
u1=True
print(u1)
print()
obj=not u1
print(obj)
print()

#Binary operator(two variables with one operator)
print("Binary operator")
b1=10
b2=20
sum=b1+b2
print(sum)
print()

#Ternary operator(one (or) more than variable with one (or) more than )conditions
print("Ternary operator")
t1=10
t2=20
result=t1 if t1>t2 else t2
print(result)